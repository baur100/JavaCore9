package dianalesson9;

public class Person {
    private String name;
    private String lastName;
    private int year;

    public void setName(String n){
        name = n;
    }
    public String getName(){
        return name;
    }
    public void setLastName(String ln){
        lastName = ln;
    }
public String getLastName(){
        return lastName;
}
public void setYear(int y){

        year=y;
}
public int getYear(){
        return year;
}

}
