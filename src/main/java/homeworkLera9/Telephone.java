package homeworkLera9;

public class Telephone {
    private String model;
    private int year;
    private String size;

    public void setModel(String model) {
        this.model = model;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getModel() {
        return model;
    }

    public int getYear() {
        return year;
    }

    public String getSize() {
        return size;
    }


}
